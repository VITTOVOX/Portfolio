# We are testing the replace, Upper , reverse  function 

#Step 1 we create a variable

story = "The!brown!fox!jumps!over!the!lazy!dog."

#Step 2 we create new variable to replace "!"

new_story = story.replace("!" , " ")

#Step 3 now we print our new sentance 

print(new_story)

#Step 4 now Upper

upper_story = story.upper()

#step 5 print upper

print(upper_story)

#Step 6 now reverse 

reversed_story = new_story[::-1]

#step 7 print reverse 

print(reversed_story)